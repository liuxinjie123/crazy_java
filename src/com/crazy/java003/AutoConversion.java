package com.crazy.java003;

public class AutoConversion {
    public static void main(String[] args) {
        int a = 6;
        System.out.println(" a=" + a);
        float f = a;
        System.out.println(" f=" + f);
        byte b = 9;
        System.out.println(" b=" + b);
        double d =  b;
        System.out.println(" d=" + d);
    }
}
